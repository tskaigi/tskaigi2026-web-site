import { readdirSync, writeFileSync } from "node:fs";
import { join } from "node:path";

const STAFF_DIR = join("src", "constants", "staff");
const OUTPUT_FILE = join("src", "constants", "staff.generated.ts");

const staffFiles = readdirSync(STAFF_DIR)
  .filter((file) => file.endsWith(".json"))
  .sort((a, b) => a.localeCompare(b, "en", { numeric: true }));

const imports = staffFiles.map((file) => {
  const id = file.replace(".json", "");
  return `import staff${id} from "./staff/${file}";`;
});

const entries = staffFiles.map((file) => {
  const id = file.replace(".json", "");
  return `  staff${id},`;
});

const output = `/* eslint-disable */
// This file is auto-generated by scripts/generate-staff-list.mjs.
// Do not edit manually.

${imports.join("\n")}

export const staffList = [
${entries.join("\n")}
];
`;

writeFileSync(OUTPUT_FILE, output, "utf8");
console.log(`Generated ${OUTPUT_FILE} (${staffFiles.length} entries)`);
