import { existsSync, mkdirSync, readdirSync, writeFileSync } from "node:fs";
import { join } from "node:path";

function generateList(dir, outputFile, listName, typeName, varPrefix) {
  if (!existsSync(dir)) mkdirSync(dir, { recursive: true });

  const staffFiles = readdirSync(dir)
    .filter((file) => file.endsWith(".json"))
    .sort((a, b) => a.localeCompare(b, "en", { numeric: true }));

  const imports = staffFiles.map((file) => {
    const id = file.replace(".json", "");
    return `import ${varPrefix}${id} from "./${dir.split("/").pop()}/${file}";`;
  });

  const entries = staffFiles.map((file) => {
    const id = file.replace(".json", "");
    return `  ${varPrefix}${id},`;
  });

  const output = `/* eslint-disable */
// This file is auto-generated by scripts/generate-staff-list.mjs.
// Do not edit manually.

${imports.join("\n")}

export type ${typeName} = {
  name: string;
  image: string;
  href?: string;
};


export const ${listName}: ${typeName}[] = [
${entries.join("\n")}
];
`;

  writeFileSync(outputFile, output, "utf8");
  console.log(`Generated ${outputFile} (${staffFiles.length} entries)`);
}

generateList(
  join("src", "constants", "staff"),
  join("src", "constants", "staff.generated.ts"),
  "STAFF_LIST",
  "Staff",
  "staff",
);

generateList(
  join("src", "constants", "day-staff"),
  join("src", "constants", "day-staff.generated.ts"),
  "DAY_STAFF_LIST",
  "DayStaff",
  "dayStaff",
);
